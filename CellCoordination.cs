
namespace Balls
{
    class CellCoordination
    {
        int column=0;
        int row;
        public void setColumn(int column)
        { this.column = column; }
        public void setRow(int row)
        { this.row = row; }
        public int getColumn()
        { return this.column; }
        public int getRow()
        { return this.row; }
    }
}
